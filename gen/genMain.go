package gen

import (
	"log"
	"os"
	"text/template"
)

var (
	gingerTemplateMain = template.Must(template.New("generate_helloworld").Parse(
		`// generated by ginger from go generate -- DO NOT EDIT
/*
	MIT License
	Copyright Ming-der Wang<ming@log4analytics.com>
*/
package main

import "fmt"
func main() {
	fmt.Println("hello "+ {{.Name}})
}
`))
)

type Person struct {
	Name string
}

func checkError(err error, message string) {
	if err != nil {
		log.Fatal(message)
		panic(err)
	}
}

func GenMain(name string) {
	output, err := os.OpenFile("main.go", os.O_WRONLY|os.O_CREATE, 0600)
	defer output.Close()
	checkError(err, "could not open output file")
	person := Person{name}
	gingerTemplateMain.Execute(output, person)

}
